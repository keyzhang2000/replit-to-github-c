#include <stdio.h>

//Hàm cộng
int add(int a, int b){
    return a + b;
}
//Hàm trừ
int subtract(int a, int b){
    return a - b;
}
//Hàm nhân
int multiply(int a, int b){
    return a * b;
}
//Hàm chia lấy phần nguyên
int divide(int a, int b){
    return a / b;
}
//Hàm chia lấy phần dư
int modulus(int a, int b){
    return a % b;
}